// This will replace the default JSON.stringify with a version that handles circular references
const circularStringify = (obj) => {
    const seen = new WeakSet();
    return JSON.stringify(obj, (key, value) => {
      if (typeof value === 'object' && value !== null) {
        if (seen.has(value)) {
          return '[Circular]';
        }
        seen.add(value);
      }
      return value;
    });
  };
  
  global.JSON.stringify = circularStringify;